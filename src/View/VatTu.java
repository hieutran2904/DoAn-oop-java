    private void tblVatTuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblVatTuMouseClicked
        // TODO add your handling code here:
        try {
            int row = tblVatTu.getSelectedRow();
            if (row >= 0) {
                String id = (String) tblVatTu.getValueAt(row, 0);
                int c = cbxidkho.getItemCount();
                VATTU vt = new VatTuDao().findByID(id);
                System.out.println("id vat:" + id);
                if (vt != null) {
                    txtidvattu.setText(vt.getId_vt());
                    txttenvattu.setText(vt.getTen_vt());
                    txtloaivt.setText(vt.getLoai_vt());
                    txtdvt.setText(vt.getDon_vi_tinh());
                    System.out.println("id kho : "+vt.getId_kho());
                    for(int i =0  ; i < c; i++){
                        if(vt.getId_kho() == null ? (String) cbxidkho.getItemAt(i) == null : vt.getId_kho().equals((String) cbxidkho.getItemAt(i))){
                            cbxidkho.setSelectedIndex(i); 
                            break;
                        }
                    }
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_tblVatTuMouseClicked

