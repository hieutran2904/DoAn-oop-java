    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
        StringBuilder sb = new StringBuilder();
        if(txtidvattu.getText().equals("")) sb.append("Ban chua nhap ID vat tu");
        else if(txttenvattu.getText().equals("")) sb.append("Ban chua nhap ten vat tu");
        else if(txtloaivt.getText().equals("")) sb.append("Ban chua nhap loai vat tu");
        else if(txtdvt.getText().equals("")) sb.append("Ban chua nhap dia don vi tinh");
        if(sb.length()>0){
           JOptionPane.showMessageDialog(this, sb);
           return;
        }
        try {
            VATTU vt = new VATTU();
            String id_kho = (String) cbxidkho.getItemAt(cbxidkho.getSelectedIndex());
            vt.setId_vt(txtidvattu.getText());
            vt.setTen_vt(txttenvattu.getText());
            vt.setLoai_vt(txtloaivt.getText());
            vt.setDon_vi_tinh(txtdvt.getText());
            vt.setId_kho(id_kho);
            if(new VatTuDao().insert(vt)){
                JOptionPane.showMessageDialog(this,"Vat tu da duoc them vao CSDL");
                loadDataToTable();
            }
            else{
                JOptionPane.showMessageDialog(this,"Them khong thanh cong");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "ERROR: "+e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnLuuActionPerformed

