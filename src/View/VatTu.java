    private void loadDataToTable(){
        try {
            String key = txttimkiem.getText();
            VatTuDao dao = new VatTuDao();
            List<VATTU> list = dao.findAll(key);
            tblModel.setRowCount(0);
            for(VATTU vt : list){
                tblModel.addRow(new Object[]{
                    vt.getId_vt(),
                    vt.getTen_vt(),
                    vt.getLoai_vt(),
                    vt.getDon_vi_tinh(),
                    vt.getId_kho()
                });
            }
            System.out.println("id vat tu: "+key);
            tblModel.fireTableDataChanged();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    private void initComboBox(){
        try {
            String sql = "SELECT id_kho FROM KHO";
            Connection conn = ConnectDB.getConnection();
            PreparedStatement pstmt = conn.prepareStatement(sql);
            ResultSet rs = pstmt.executeQuery();
            cbxidkho.removeAllItems();
            while(rs.next()){
                cbxidkho.addItem(rs.getString("id_kho"));
            }
            rs.close();
            pstmt.close();
            conn.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
            e.printStackTrace();
        }
    }
    
