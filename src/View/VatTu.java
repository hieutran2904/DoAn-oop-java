    private String CreateID() throws Exception{
        List<VATTU> list = new VatTuDao().findAll("");  
        if (true) {
            int index = list.size()-1;
            VATTU vt = list.get(index);
            int idNumber = Integer.parseInt(vt.getId_vt().substring(2)) + 1; //id next        
            if (idNumber < 10) {
                return "vt00"+String.valueOf(idNumber);
            }
            else if (idNumber >= 10 && idNumber <100) {
                return "vt0"+String.valueOf(idNumber);
            }
            return "vt"+String.valueOf(idNumber);
        }
        return "vt001";
    }
    
