    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
        if(JOptionPane.showConfirmDialog(this,"Ban co muon thuc hien?") == JOptionPane.NO_OPTION){
            return;
        }
        NHANVIEN nv = new NhanVienDao().findByID(Login.id);
        System.out.println("mkc: " + nv.getMat_khau());
        System.out.println("mkc text: "+ String.valueOf(txtmkc.getPassword()));
        if (CheckNull()) {
            if (nv.getMat_khau().equals(String.valueOf(txtmkc.getPassword()))) {
                try {
                    NHANVIEN nhanvien = new NHANVIEN();
                    nhanvien.setId_nv(Login.id);
                    nhanvien.setMat_khau(String.valueOf(txtmkm.getPassword()));
                    if(new NhanVienDao().changePassword(nhanvien)){
                        JOptionPane.showMessageDialog(this,"Doi mat khau thanh cong!");
                        jlbstatus.setText("Trang thai");
                    }
                    else{
                        JOptionPane.showMessageDialog(this,"Doi mat khau khong thanh cong");
                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "ERROR: "+e.getMessage());
                    e.printStackTrace();
                }
            } else{
                jlbstatus.setText("Ban da nhap sai mat khau cu!");
            }
        }
    }//GEN-LAST:event_btnLuuActionPerformed

