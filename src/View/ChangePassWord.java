    private boolean CheckNull(){
        boolean kq=true;
        if(String.valueOf(txtmkc.getPassword()).length()==0){
            jlbstatus.setText("Ban chua nhap mat khau cu");
            return false;
        }
        if(String.valueOf(txtmkm.getPassword()).length()==0){
            jlbstatus.setText("Ban chua nhap mat khau moi");
            return false;
        }
        return kq;
    }
