    private void loadDataToTable(){
        try {
            String key = txttimkiem.getText();
            PhieuNhapDao dao = new PhieuNhapDao();
            List<PHIEUNHAP> list = dao.findAll(key);
            tblModel.setRowCount(0);
            for(PHIEUNHAP pn : list){
                if (pn.getId_nv().contains(Login.id)) {
                    tblModel.addRow(new Object[]{
                    pn.getId_pn(),
                    pn.getId_vt(),
                    pn.getId_nhacc(),
                    pn.getId_nv(),
                    pn.getNgay_nhap(),
                    pn.getSo_luong_nhap(),
                    pn.getDon_gia_nhap(),
                    pn.getVi_tri_vat_tu()
                    });
                }
                else if (Login.id.contains("ad")) {
                    tblModel.addRow(new Object[]{
                    pn.getId_pn(),
                    pn.getId_vt(),
                    pn.getId_nhacc(),
                    pn.getId_nv(),
                    pn.getNgay_nhap(),
                    pn.getSo_luong_nhap(),
                    pn.getDon_gia_nhap(),
                    pn.getVi_tri_vat_tu()
                    });
                }
            }
            tblModel.fireTableDataChanged();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
 
    private void initComboBox() throws Exception{
        List<VATTU> listvt = new VatTuDao().findAll("");
        List<NHACC> listncc = new NhaCCDao().findAll();
        cbxidvt.removeAllItems();
        cbxidnhacc.removeAllItems();
        for(VATTU vt : listvt){
            cbxidvt.addItem(vt.getId_vt());
        }
        for(NHACC ncc : listncc){
            cbxidnhacc.addItem(ncc.getId_nhacc());
        }
    }
