    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
        StringBuilder sb = new StringBuilder();
        if(txtidpn.getText().equals("")){
            sb.append("Ban chua nhap ID phieu nhap");
        }
        else if (txtsoluongnhap.getText().equals("")) {
            sb.append("Ban chua nhap so luong nhap");
        }
        else if (txtdongianhap.getText().equals("")) {
            sb.append("Ban chua nhap so don gia nhap");
        }
        else if (txtvitri.getText().equals("")) {
            sb.append("Ban chua nhap so luong nhap");
        }
        else if (new SimpleDateFormat("yyyy-MM-dd").format(jdcngaynhap.getDate()).equals("")) {
            sb.append("Ban chua nhap ngay nhap kho");
        }
        if(sb.length()>0){
           JOptionPane.showMessageDialog(this, sb);
           return;
        }
        try {
            PHIEUNHAP pn = new PHIEUNHAP();
            String id_vt = (String) cbxidvt.getItemAt(cbxidvt.getSelectedIndex());
            String id_nhacc = (String) cbxidnhacc.getItemAt(cbxidnhacc.getSelectedIndex());
            pn.setId_pn(txtidpn.getText());
            pn.setId_vt(id_vt);
            pn.setId_nhacc(id_nhacc);
            pn.setId_nv(Login.id);
            pn.setNgay_nhap(new SimpleDateFormat("yyyy-MM-dd").format(jdcngaynhap.getDate()));
            pn.setSo_luong_nhap(Integer.parseInt(txtsoluongnhap.getText()));
            pn.setDon_gia_nhap(Integer.parseInt(txtdongianhap.getText()));
            pn.setVi_tri_vat_tu(txtvitri.getText());
            if (new PhieuNhapDao().insert(pn)) {
                JOptionPane.showMessageDialog(this,"Phieu nhap da duoc them vao CSDL");
                loadDataToTable();
            }
            else{
                JOptionPane.showMessageDialog(this,"Them khong thanh cong");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "ERROR: "+e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnLuuActionPerformed

