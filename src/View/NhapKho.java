    private void tblPhieuNhapMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPhieuNhapMouseClicked
        // TODO add your handling code here:
        try {
            int row = tblPhieuNhap.getSelectedRow();
            int count_vt = cbxidvt.getItemCount();
            int count_nhacc = cbxidnhacc.getItemCount();

            if (row >= 0) {
                String id = (String) tblPhieuNhap.getValueAt(row, 0);
                PHIEUNHAP pn = new PhieuNhapDao().findByID(id);
                if (pn != null) {
                    txtidpn.setText(pn.getId_pn());
                    for(int i = 0; i < count_vt; i++){
                        if(pn.getId_vt() == null ? cbxidvt.getItemAt(i) == null : pn.getId_vt().equals(cbxidvt.getItemAt(i))){
                            cbxidvt.setSelectedIndex(i);
                        }
                    }
                    for(int i = 0; i < count_nhacc; i++){
                        if(pn.getId_nhacc() == null ? cbxidnhacc.getItemAt(i) == null : pn.getId_nhacc().equals(cbxidnhacc.getItemAt(i))){
                            cbxidnhacc.setSelectedIndex(i);
                        }
                    }
                    txtidnv.setText(pn.getId_nv());
                    jdcngaynhap.setDate(new SimpleDateFormat("yyyy-MM-dd").parse(pn.getNgay_nhap()));
                    txtsoluongnhap.setText(String.valueOf(pn.getSo_luong_nhap()));
                    txtdongianhap.setText(String.valueOf(pn.getDon_gia_nhap()));
                    txtvitri.setText(pn.getVi_tri_vat_tu());
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_tblPhieuNhapMouseClicked


