    private String CreateID() throws Exception{
        List<PHIEUNHAP> list = new PhieuNhapDao().findAll("");  
        if (list.size()!=0) {
            int index = list.size()-1;
            PHIEUNHAP pn = list.get(index);
            int idNumber = Integer.parseInt(pn.getId_pn().substring(2)) + 1; //id next        
            if (idNumber < 10) {
                return "pn00"+String.valueOf(idNumber);
            }
            else if (idNumber >= 10 && idNumber <100) {
                return "pn0"+String.valueOf(idNumber);
            }
            return "pn"+String.valueOf(idNumber);
        }
        return "pn001";
    }
    
