    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
        StringBuilder sb = new StringBuilder();
        if(txtidkho.getText().equals("")) sb.append("Ban chua nhap ID kho");
        else if(txttenkho.getText().equals("")) sb.append("Ban chua nhap ten kho");
        else if(txtloaikho.getText().equals("")) sb.append("Ban chua nhap loai kho");
        else if(txtdiachi.getText().equals("")) sb.append("Ban chua nhap dia chi kho");
        if(sb.length()>0){
           JOptionPane.showMessageDialog(this, sb);
           return;
        }
        try {
            KHO kho = new KHO();
            kho.setId_kho(txtidkho.getText());
            kho.setTen_kho(txttenkho.getText());
            kho.setLoai_kho(txtloaikho.getText());
            kho.setDia_chi(txtdiachi.getText());
            
            if(new KhoDao().insert(kho)){
                JOptionPane.showMessageDialog(this,"Kho da duoc them vao CSDL");
                loadDataToTable();
            }
            else{
                JOptionPane.showMessageDialog(this,"Them khong thanh cong");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "ERROR: "+e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnLuuActionPerformed

    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
        // TODO add your handling code here:
        if(JOptionPane.showConfirmDialog(this,"Ban co muon cap nhat kho hang khong?") == JOptionPane.NO_OPTION){
            return;
        }
        try {
            KHO kho = new KHO();
            kho.setId_kho(txtidkho.getText());
            kho.setTen_kho(txttenkho.getText());
            kho.setLoai_kho(txtloaikho.getText());
            kho.setDia_chi(txtdiachi.getText());
            if (new KhoDao().update(kho)) {
                JOptionPane.showMessageDialog(this,"kho da duoc cap nhat vao CSDL");
                loadDataToTable();
            }
            else{
                JOptionPane.showMessageDialog(this,"Cap nhat khong thanh cong");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "ERROR: "+e.getMessage());
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_btnCapNhatActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        if(JOptionPane.showConfirmDialog(this,"Ban co muon xoa kho khong?") == JOptionPane.NO_OPTION){
            return;
        }
        try {
            if (new KhoDao().delete(txtidkho.getText())) {
                JOptionPane.showMessageDialog(this,"Kho da duoc xoa khoi CSDL");
                btnMoiActionPerformed(evt);
                loadDataToTable();
            }
            else{
                JOptionPane.showMessageDialog(this,"Xoa khong thanh cong");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void tblkhoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblkhoMouseClicked
        // TODO add your handling code here:
        try {
            int row = tblkho.getSelectedRow();
            if(row >= 0){
                String id = (String) tblkho.getValueAt(row,0);
                KhoDao dao = new KhoDao();
                KHO kho = dao.findByID(id);
                System.out.println("id kho: "+id);

                if(kho != null){
                    txtidkho.setText(kho.getId_kho());
                    txttenkho.setText(kho.getTen_kho());
                    txtloaikho.setText(kho.getLoai_kho());
                    txtdiachi.setText(kho.getDia_chi());
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_tblkhoMouseClicked


