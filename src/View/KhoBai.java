    private String CreateID() throws Exception{
        List<KHO> list = new KhoDao().findAll();  
        if (list.size()!=0) {
            int index = list.size()-1;
            KHO kho = list.get(index);
            int idNumber = Integer.parseInt(kho.getId_kho().substring(3)) + 1; //id next        
            if (idNumber < 10) {
                return "kho0"+String.valueOf(idNumber);
            }
            return "kho"+String.valueOf(idNumber);
        }
        return "kho01";
    }
    
    private void initTable(){
        tblModel = new DefaultTableModel();
        tblModel.setColumnIdentifiers(new String[]{"ID kho","Ten kho","Loai Kho","Dia chi"});
        tblkho.setModel(tblModel);
    }
    
    private void loadDataToTable(){
        try {
            KhoDao dao = new KhoDao();
            List<KHO> list = dao.findAll();
            tblModel.setRowCount(0);
            for(KHO kho : list){
                tblModel.addRow(new Object[]{
                    kho.getId_kho(),
                    kho.getTen_kho(),
                    kho.getLoai_kho(),
                    kho.getDia_chi()
                });
            }
            tblModel.fireTableDataChanged();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
