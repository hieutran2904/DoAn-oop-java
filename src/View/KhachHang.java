    private String CreateID() throws Exception{
        List<KHACHHANG> list = new KhachHangDao().findAll();  
        if (list.size()!=0) {
            int index = list.size()-1;
            KHACHHANG kh = list.get(index);
            int idNumber = Integer.parseInt(kh.getId_kh().substring(2)) + 1; //id next        
            if (idNumber < 10) {
                return "kh00"+String.valueOf(idNumber);
            }
            else if (idNumber >= 10 && idNumber <100) {
                return "kh0"+String.valueOf(idNumber);
            }
            return "kh"+String.valueOf(idNumber);
        }
        return "kh001";
    }
    
    private void initTable(){
        tblModel = new DefaultTableModel();
        tblModel.setColumnIdentifiers(new String[]{"ID khach hang","Ho va Ten","Phai","So dien thoai"});
        tblKhachHang.setModel(tblModel);
    }
    private void loadDataToTable(){
        try {
            KhachHangDao dao = new KhachHangDao();
            List<KHACHHANG> list = dao.findAll();
            tblModel.setRowCount(0);
            for(KHACHHANG kh : list){
                tblModel.addRow(new Object[]{
                    kh.getId_kh(),
                    kh.getHo_ten_hk(),
                    kh.getPhai(),
                    kh.getStd()
                });
            }
            tblModel.fireTableDataChanged();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
