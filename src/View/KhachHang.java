    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
        StringBuilder sb = new StringBuilder();
        if(txtidkh.getText().equals("")){
            sb.append("Ban chua nhap ID khach hang");
        }
        else if(txthotenkh.getText().equals("")){
            sb.append("Ban chua nhap ho ten khach hang");
        }
        else if(!jrbNam.isSelected() && !jrbNu.isSelected()){
            sb.append("Ban chua chon gioi tinh");
        }
        else if(txtsdt.getText().equals("")){
            sb.append("Ban chua nhap so dien thoai");
        }
        if(sb.length()>0){
           JOptionPane.showMessageDialog(this, sb);
           return;
        }
        try {
            KHACHHANG kh = new KHACHHANG();
            kh.setId_kh(txtidkh.getText());
            kh.setHo_ten_hk(txthotenkh.getText());
            kh.setPhai(jrbNam.isSelected() ? "Nam" : "Nu");
            kh.setStd(txtsdt.getText());
            
            if (new KhachHangDao().insert(kh)) {
                JOptionPane.showMessageDialog(this,"Khach hang da duoc them vao CSDL");
                loadDataToTable();
            }
            else{
                JOptionPane.showMessageDialog(this,"Them khong thanh cong");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "ERROR: "+e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnLuuActionPerformed

    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
        if(JOptionPane.showConfirmDialog(this,"Ban co muon cap nhat khach hang khong?") == JOptionPane.NO_OPTION){
            return;
        }
        try {
            KHACHHANG kh = new KHACHHANG();
            kh.setId_kh(txtidkh.getText());
            kh.setHo_ten_hk(txthotenkh.getText());
            kh.setPhai(jrbNam.isSelected() ? "Nam" : "Nu");
            kh.setStd(txtsdt.getText());
            if (new KhachHangDao().update(kh)) {
                JOptionPane.showMessageDialog(this,"khach hang da duoc cap nhat vao CSDL");
                loadDataToTable();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "ERROR: "+e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnCapNhatActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        if(JOptionPane.showConfirmDialog(this,"Ban co muon xoa khach hang khong?") == JOptionPane.NO_OPTION){
            return;
        }
        try {
            if (new KhachHangDao().delete(txtidkh.getText())) {
                JOptionPane.showMessageDialog(this,"Khach hang da duoc xoa khoi CSDL");
                btnMoiActionPerformed(evt);
                loadDataToTable();
            }
            else{
                JOptionPane.showMessageDialog(this,"Xoa khong thanh cong");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void tblKhachHangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblKhachHangMouseClicked
        // TODO add your handling code here:
        try {
            int row = tblKhachHang.getSelectedRow();
            if(row >= 0){
                String id = (String) tblKhachHang.getValueAt(row,0);
                KhachHangDao dao = new KhachHangDao();
                KHACHHANG kh = dao.findByID(id);
                System.out.println("id khach hang: "+id);

                if(kh != null){
                    txtidkh.setText(kh.getId_kh());
                    txthotenkh.setText(kh.getHo_ten_hk());
                    if(kh.getPhai().equals("Nam")) jrbNam.setSelected(true);
                    else jrbNu.setSelected(true);
                    txtsdt.setText(kh.getStd());
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_tblKhachHangMouseClicked


