    private String CreateID() throws Exception{
        List<NHANVIEN> list = new NhanVienDao().findAll(""); 
        if (list.size()!=0) {
            int index = list.size()-1;
            NHANVIEN nv = list.get(index); 
            int idNumber = Integer.parseInt(nv.getId_nv().substring(2)) + 1; //id next        
            if (idNumber < 10) {
                return "nv00"+String.valueOf(idNumber); // =>"nv002"
            }
            else if (idNumber >= 10 && idNumber <100) {
                return "nv0"+String.valueOf(idNumber);
            }
            return "nv"+String.valueOf(idNumber);
        }
        return "nv001";
    }
    
    private void initTable(){
        tblModel = new DefaultTableModel();
        tblModel.setColumnIdentifiers(new String[]{"ID nhan vien","Ho","Ten","Ngay sinh","Phai","Sdt","Tai_khoan","Ma khau"});
        tblNhanVien.setModel(tblModel);
    }
    
    private void loadDataToTable(){
        try {
            String key = txttimkiem.getText();
            NhanVienDao dao = new NhanVienDao();
            List<NHANVIEN> list = dao.findAll(key);
            tblModel.setRowCount(0);
            for(NHANVIEN nv : list){
                tblModel.addRow(new Object[]{
                    nv.getId_nv(),
                    nv.getHo_nv(),
                    nv.getTen_nv(),
                    nv.getNgay_sinh(),
                    nv.getPhai(),
                    nv.getStd(),
                    nv.getTai_khoan(),
                    nv.getMat_khau()
                });
            }
            //System.out.println("tu tim kiem : " + key);
            tblModel.fireTableDataChanged();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
