    private void tblNhanVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblNhanVienMouseClicked
        // TODO add your handling code here:
        try {
            int row = tblNhanVien.getSelectedRow();
            if(row >= 0){
                String id = (String) tblNhanVien.getValueAt(row,0);
                NhanVienDao dao = new NhanVienDao();
                System.out.println("id nhan vien: "+id);
                System.out.println("dong so: "+row);
                NHANVIEN nv = dao.findByID(id);
                if(nv != null){
                    txtidnv.setText(nv.getId_nv());
                    txthonv.setText(nv.getHo_nv());
                    txttennv.setText(nv.getTen_nv());
                    jdcngaysinh.setDate(new SimpleDateFormat("yyyy-MM-dd").parse(nv.getNgay_sinh()));
                    if(nv.getPhai().equals("Nam")) jrbNam.setSelected(true);
                    else jrbNu.setSelected(true);
                    txtsdt.setText(nv.getStd());
                    txttk.setText(nv.getTai_khoan());
                    txtmk.setText(nv.getMat_khau());
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_tblNhanVienMouseClicked

