    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        StringBuilder sb = new StringBuilder();
        if(txthonv.getText().equals("")){
            sb.append("Ban chua nhap ho nhan vien");
        }
        else if(txttennv.getText().equals("")){
            sb.append("Ban chua nhap ten nhan vien");
        }
//        else if(dateFormat.format(jdcngaysinh.getDate()).equals("")){
//            sb.append("Ban chua chon ngay sinh");
//        } //error
        else if(!jrbNam.isSelected() && !jrbNu.isSelected()){
            sb.append("Ban chua chon gioi tinh");
        }
        else if(txtsdt.getText().equals("")){
            sb.append("Ban chua nhap sdt nhan vien");
        }
        else if(txttk.getText().equals("")){
            sb.append("Ban chua nhap ten dang nhap nhan vien");
        }
        else if(txtmk.getText().equals("")){
            sb.append("Ban chua nhap mat khau nhan vien");
        }
        if(sb.length()>0){
           JOptionPane.showMessageDialog(this, sb);
           return;
        }
        try {
            NHANVIEN nhanvien = new NHANVIEN();
            nhanvien.setId_nv(txtidnv.getText());
            nhanvien.setHo_nv(txthonv.getText());
            nhanvien.setTen_nv(txttennv.getText());
            nhanvien.setNgay_sinh(new SimpleDateFormat("yyyy-MM-dd").format(jdcngaysinh.getDate()));
            nhanvien.setPhai(jrbNam.isSelected()? "Nam" : "Nu");
            nhanvien.setStd(txtsdt.getText());
            nhanvien.setTai_khoan(txttk.getText());
            nhanvien.setMat_khau(txtmk.getText());
            if(new NhanVienDao().insert(nhanvien)){
                JOptionPane.showMessageDialog(this,"Nhan vien da duoc them vao CSDL");
                loadDataToTable();
            }
            else{
                JOptionPane.showMessageDialog(this,"Them khong thanh cong");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "ERROR: "+e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnLuuActionPerformed

