    private String CreateID() throws Exception{
        List<PHIEUXUAT> list = new PhieuXuatDao().findAll("");  
        if (list.size()!=0) {
            int index = list.size()-1;
            PHIEUXUAT px = list.get(index);
            int idNumber = Integer.parseInt(px.getId_px().substring(2)) + 1; //id next        
            if (idNumber < 10) {
                return "px00"+String.valueOf(idNumber);
            }
            else if (idNumber >= 10 && idNumber <100) {
                return "px0"+String.valueOf(idNumber);
            }
            return "px"+String.valueOf(idNumber);
            }
        return "px001";
    }
    
