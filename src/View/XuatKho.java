    private void tblPhieuXuatMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPhieuXuatMouseClicked
        // TODO add your handling code here:
        try {
            int row = tblPhieuXuat.getSelectedRow();
            int count_vt = cbxidvt.getItemCount();
            int count_kh = cbxidkh.getItemCount();

            if (row >= 0) {
                String id = (String) tblPhieuXuat.getValueAt(row, 0);
                PHIEUXUAT pn = new PhieuXuatDao().findByID(id);
                if (pn != null) {
                    txtidpx.setText(pn.getId_px());
                    for(int i = 0; i < count_vt; i++){
                        if(pn.getId_vt() == null ? cbxidvt.getItemAt(i) == null : pn.getId_vt().equals(cbxidvt.getItemAt(i))){
                            cbxidvt.setSelectedIndex(i);
                        }
                    }
                    for(int i = 0; i < count_kh; i++){
                        if(pn.getId_kh() == null ? cbxidkh.getItemAt(i) == null : pn.getId_kh().equals(cbxidkh.getItemAt(i))){
                            cbxidkh.setSelectedIndex(i);
                        }
                    }
                    txtidnv.setText(pn.getId_nv());
                    jdcngayxuat.setDate(new SimpleDateFormat("yyyy-MM-dd").parse(pn.getNgay_xuat()));
                    txtsoluongxuat.setText(String.valueOf(pn.getSo_luong_xuat()));
                    txtdongiaxuat.setText(String.valueOf(pn.getDon_gia_xuat()));
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_tblPhieuXuatMouseClicked


