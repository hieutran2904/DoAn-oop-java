    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
        StringBuilder sb = new StringBuilder();
        if(txtidpx.getText().equals("")){
            sb.append("Ban chua nhap ID phieu xuat");
        }
        else if (txtsoluongxuat.getText().equals("")) {
            sb.append("Ban chua nhap so luong xuat");
        }
        else if (txtdongiaxuat.getText().equals("")) {
            sb.append("Ban chua nhap so luong xuat");
        }
        else if (new SimpleDateFormat("yyyy-MM-dd").format(jdcngayxuat.getDate()).equals("")) {
            sb.append("Ban chua nhap ngay xuat kho");
        }
        if(sb.length()>0){
           JOptionPane.showMessageDialog(this, sb);
           return;
        }
        try {
            PHIEUXUAT px = new PHIEUXUAT();
            String id_vt = (String) cbxidvt.getItemAt(cbxidvt.getSelectedIndex());
            String id_kh = (String) cbxidkh.getItemAt(cbxidkh.getSelectedIndex());
            px.setId_px(txtidpx.getText());
            px.setId_vt(id_vt);
            px.setId_kh(id_kh);
            px.setId_nv(Login.id);
            px.setNgay_xuat(new SimpleDateFormat("yyyy-MM-dd").format(jdcngayxuat.getDate()));
            px.setSo_luong_xuat(Integer.parseInt(txtsoluongxuat.getText()));
            px.setDon_gia_xuat(Integer.parseInt(txtdongiaxuat.getText()));
            
            TONGVATTU ttv = new TongVatTuDao().checkSL(id_vt);
            int slmax = ttv.getTong_sln() - ttv.getTong_slx() - ttv.getTong_slt();
            if (Integer.parseInt(txtsoluongxuat.getText()) > slmax) {
                JOptionPane.showMessageDialog(this,"so luong xuat <= " + String.valueOf(slmax) ,"canh bao",JOptionPane.ERROR_MESSAGE);
            } else{
                if (new PhieuXuatDao().insert(px)) {
                JOptionPane.showMessageDialog(this,"Phieu xuat da duoc them vao CSDL");
                loadDataToTable();
                }
                else{
                    JOptionPane.showMessageDialog(this,"Them khong thanh cong");
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "ERROR: "+e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnLuuActionPerformed

