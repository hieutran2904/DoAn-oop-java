    private void loadDataToTable(){
        try {
            String key = txttimkiem.getText();
            PhieuXuatDao dao = new PhieuXuatDao();
            List<PHIEUXUAT> list = dao.findAll(key);
            tblModel.setRowCount(0);
            for(PHIEUXUAT px : list){
                if (px.getId_nv().contains(Login.id)) {
                    tblModel.addRow(new Object[]{
                    px.getId_px(),
                    px.getId_vt(),
                    px.getId_kh(),
                    px.getId_nv(),
                    px.getNgay_xuat(),
                    px.getSo_luong_xuat(),
                    px.getDon_gia_xuat(),
                    });
                }
                else if (Login.id.contains("ad")) {
                    tblModel.addRow(new Object[]{
                    px.getId_px(),
                    px.getId_vt(),
                    px.getId_kh(),
                    px.getId_nv(),
                    px.getNgay_xuat(),
                    px.getSo_luong_xuat(),
                    px.getDon_gia_xuat(),
                    });
                }
            }
            tblModel.fireTableDataChanged();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    private void initComboBox() throws Exception{
        List<VATTU> listvt = new VatTuDao().findCBX();
        List<KHACHHANG> listkh = new KhachHangDao().findAll();
        cbxidvt.removeAllItems();
        cbxidkh.removeAllItems();
        for(VATTU vt : listvt){
            cbxidvt.addItem(vt.getId_vt());
        }
        for(KHACHHANG kh : listkh){
            cbxidkh.addItem(kh.getId_kh());
        }
    }
